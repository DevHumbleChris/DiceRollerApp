@Composable
fun DiceWithButtonAndImage(modifier: Modifier) {
    var result by remember {
        mutableStateOf(1)
    }
    var isButtonRolled by remember {
        mutableStateOf(false)
    }
    val imageResource = when(result) {
        1 -> R.drawable.dice_1
        2 -> R.drawable.dice_2
        3 -> R.drawable.dice_3
        4 -> R.drawable.dice_4
        5 -> R.drawable.dice_5
        else -> R.drawable.dice_6
    }
    Column (
        modifier = modifier,
        horizontalAlignment = Alignment.CenterHorizontally
    ) {
        if (!isButtonRolled) {
            Text(text = "Please Roll The Dice")
        } else {
            Text(text = "Rolled Dice To: $result")
        }
        Image(
            painter = painterResource(id = imageResource),
            contentDescription = "Dice 1"
        )
        Button(onClick = {
            result = (1..6).random()
            isButtonRolled = true
        }) {
            Text(text = "Roll")
        }
    }
}

@Preview(showBackground = true)
@Composable
fun DiceRollerApp() {
    DiceWithButtonAndImage(
        modifier = Modifier
            .fillMaxSize()
            .wrapContentSize(Alignment.Center)
    )
}